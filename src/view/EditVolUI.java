    private void btnSaveVolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveVolActionPerformed
 String dateDep  = txtDateDepVol.getText().trim();
    String heureDep = txtHeureDepVol.getText().trim();
    String dateArr  = txtDateArrVol.getText().trim();
    String heureArr = txtHeureArrVol.getText().trim();

    if (dateDep.isEmpty() || heureDep.isEmpty()
            || dateArr.isEmpty() || heureArr.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Tous les champs de date/heure sont obligatoires.");
        return;
    }

    try {
        Vol v = new Vol();
        v.setDateDepart(LocalDate.parse(dateDep));
        v.setHeureDepart(LocalTime.parse(heureDep));
        v.setDateArrivee(LocalDate.parse(dateArr));
        v.setHeureArrivee(LocalTime.parse(heureArr));
        v.setReservable(chkReservableVol.isSelected());

        Aeroport dep = (Aeroport) cbAeroDepVol.getSelectedItem();
        Aeroport arr = (Aeroport) cbAeroArrVol.getSelectedItem();
        if (dep == null || arr == null) {
            JOptionPane.showMessageDialog(this,
                    "Choisissez l'aéroport de départ et d'arrivée.");
            return;
        }
        v.setAeroportDepart(dep);
        v.setAeroportArrivee(arr);

        volDAO.update(v, idVol);
        JOptionPane.showMessageDialog(this, "Vol modifié avec succès.");
        dispose();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,
                "Erreur : " + ex.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveVolActionPerformed

