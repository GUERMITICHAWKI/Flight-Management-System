   public EditVolUI(int idVol) {
    this.idVol = idVol;
    initComponents();
    setLocationRelativeTo(null);

    // 1) Charger tous les aéroports dans les combos
    List<Aeroport> lst = aerDAO.getAll();
    cbAeroDepVol.removeAllItems();
    cbAeroArrVol.removeAllItems();
    for (Aeroport a : lst) {
        cbAeroDepVol.addItem(a);
        cbAeroArrVol.addItem(a);
    }

    // 2) Charger le vol à éditer
    volOriginal = volDAO.findById(idVol);
    if (volOriginal != null) {
        txtDateDepVol.setText(volOriginal.getDateDepart().toString());
        txtHeureDepVol.setText(volOriginal.getHeureDepart().toString());
        txtDateArrVol.setText(volOriginal.getDateArrivee().toString());
        txtHeureArrVol.setText(volOriginal.getHeureArrivee().toString());
        chkReservableVol.setSelected(volOriginal.isReservable());

        // 3) Sélectionner les bons aéroports dans les combos
        Aeroport dep = volOriginal.getAeroportDepart();
        Aeroport arr = volOriginal.getAeroportArrivee();
        if (dep != null) {
            cbAeroDepVol.setSelectedItem(dep);
        }
        if (arr != null) {
            cbAeroArrVol.setSelectedItem(arr);
        }
    }
}



